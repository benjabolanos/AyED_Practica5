    private void botonListadoMouseEntered(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_botonListadoMouseEntered
        //Efecto hover
        if(!viendoListado){
            botonListado.setBackground(OSCURO_HOVER);
            labelListado.setForeground(CLARO_HOVER);
        }
    }//GEN-LAST:event_botonListadoMouseEntered

    private void botonListadoMouseExited(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_botonListadoMouseExited
        if(!viendoListado){
            botonListado.setBackground(OSCURO);
            labelListado.setForeground(CLARO);
        }
    }//GEN-LAST:event_botonListadoMouseExited

